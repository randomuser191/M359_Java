package Unit2.Examples.MathExamples;


public class MethodsDemonstration {
    public static void main(String[] args) {
        System.out.println("The absolute value of -8393.938040480 is " + Math.abs(-8393.938040480));
        System.out.println("The larger number between 5 and 10 is " + Math.max(5, 10));
        System.out.println("The smaller number between 5 and 10 is " + Math.min(5, 10));
        System.out.println("5 raised to the 2nd power is " + Math.pow(5, 2));
    }
}
